#Neljännen luvun ensimmäinen tehtävä on yksinkertaisen while-rakenteen toteuttaminen. Tehtävänä onkin luoda ohjelma, joka tulostaa joka kierroksella kierrosluvun, millä tällä hetkellä ollan. Ohjelma aloittaa kierroksesta 0, ja jatkaa tästä eteenpäin 4 kierrosta.
kierroksia = 5

kierros = 0

while kierros < kierroksia:
	print ("Olemme kierroksella",kierros)
	
	kierros +=1